package com.jqlmh.exercise.practice;

public class Cutomer {
	private int id;
	private String name;
	public Cutomer() {
		super();
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
